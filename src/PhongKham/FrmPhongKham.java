    public void fillDataTable(){ 
        DefaultTableModel tbModel = (DefaultTableModel) TbDSPhongKham.getModel();
        tbModel.setRowCount(0); // Xoá data trong table
        for (ClassPhongKham pk : daoPK.getAllPhongKham()){
            Object dataRow[] = new Object[2];
            dataRow[0] = pk.getMaPK();
            dataRow[1] = pk.getTenPK();
   
            tbModel.addRow(dataRow);
        }                       
    }
   
