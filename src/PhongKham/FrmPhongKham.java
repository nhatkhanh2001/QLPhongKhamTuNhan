    // Lấy dữ liệu đã nhập get vào TbPhongKham
    public ClassPhongKham getModel() throws ParseException{
        ClassPhongKham pk = new ClassPhongKham();
        pk.setMaPK(Integer.parseInt(txtMaPK.getText().trim()));
        pk.setTenPK(txtTenPK.getText().trim());
         
        return pk;
    }
    
    // Đổ dữ liệu lên form, dùng cho envent Mouse Click và tìm kiếm 
