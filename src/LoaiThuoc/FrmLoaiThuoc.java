    public void fillDataTable(){ 
        DefaultTableModel tbModel = (DefaultTableModel) TbDSLoaiThuoc.getModel();
        tbModel.setRowCount(0); // Xoá data trong table
        for (ClassLoaiThuoc lt : ltDAO.getAllLoaiThuoc()){
            Object dataRow[] = new Object[2];
            dataRow[0] = lt.getMaThuoc();
            dataRow[1] = lt.getTenThuoc();
   
            tbModel.addRow(dataRow);
        }                       
    }
   
    // Hàm bool kiểm tra đúng, sai. Nhập đầy đủ thông tin mới cho thêm loại thuốc mới
    public boolean checkValidateForm(){
        if (txtMaThuoc.getText().isEmpty()||
            txtTenThuoc.getText().isEmpty())
       
        {
           // JOptionPane.showMessageDialog(this, "Bạn chưa nhập đủ thông tin!");
            return false;
        }
        else  
        {
            return true;                
        }     
    }
    
    // Lấy dữ liệu đã nhập get vào TbDSLoaiThuoc
    public ClassLoaiThuoc getModel() throws ParseException{
        ClassLoaiThuoc lt = new ClassLoaiThuoc();
        lt.setMaThuoc(Integer.parseInt(txtMaThuoc.getText().trim()));
        lt.setTenThuoc(txtTenThuoc.getText().trim());
         
        return lt;
    }
    
    // Đổ dữ liệu lên form, dùng cho envent Mouse Click và tìm kiếm 
    public void setModel (ClassLoaiThuoc lt){
        txtMaThuoc.setText(String.valueOf(lt.getMaThuoc()));
        txtTenThuoc.setText(lt.getTenThuoc());
              
    }
    
    // Hàm thêm 1 loại thuốc mới
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
       if (!checkValidateForm())
       {
           JOptionPane.showMessageDialog(this, "Bạn chưa nhập đầy đủ thông tin!"); //Thanh thông báo hiện lên
           lbStatus.setText("Bạn chưa nhập đầy đủ thông tin!"); //Thông báo qua dòng labelStatus
           lbStatus.setForeground(Color.red);
       }
       else{
           try {
               ClassLoaiThuoc lt = getModel();
              // System.out.println("=>"+pk.toString());
               if (ltDAO.add(lt)>0) {
                   JOptionPane.showMessageDialog(this, "Thêm loại thuốc mới thành công!"); //Thanh thông báo hiện lên
                   lbStatus.setText("Thêm loại thuốc mới thành công!"); //Thông báo qua dòng labelStatus
                   lbStatus.setForeground(Color.blue);
                   fillDataTable();
                   LoadDataToArrayList(); 
               }
               else{
                   JOptionPane.showMessageDialog(this, "Thêm loại thuốc mới thất bại!"); 
                   lbStatus.setText("Thêm loại thuốc mới thất bại!");
                   lbStatus.setForeground(Color.red);
               }
           } catch (ParseException ex) {
               System.out.println("Error:"+ex.toString());
           }      
       }               
    }//GEN-LAST:event_btnThemActionPerformed

