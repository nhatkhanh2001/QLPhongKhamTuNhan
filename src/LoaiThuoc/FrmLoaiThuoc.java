    // Hàm tìm kiếm theo mã thuốc
    private void btnTimKiemTheoMaThuocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemTheoMaThuocActionPerformed
        if (txtMaThuoc.getText().isEmpty())
       {
           JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã thuốc!");
       }
    else {
        try{
            ClassLoaiThuoc findlt = ltDAO.findLoaiThuocByID(Integer.parseInt(txtMaThuoc.getText().trim()));
            
            if (findlt != null){
                JOptionPane.showMessageDialog(this, "Đã tìm thấy mã thuốc: "+txtMaThuoc.getText());
                lbStatus.setText("Đã tìm thấy mã thuốc: "+txtMaThuoc.getText());
                lbStatus.setForeground(Color.blue);
                setModel(findlt); // Tìm thấy mã thuốc sẽ đổ dữ liệu lên form
            }
            else {
                 JOptionPane.showMessageDialog(this, "Không tìm thấy mã thuốc: "+txtMaThuoc.getText());
                 lbStatus.setText("Không tìm thấy mã thuốc: "+txtMaThuoc.getText());
                 lbStatus.setForeground(Color.red);
            }
        } catch (Exception ex){
             System.out.println (ex.toString());
        }     
    }                 
    }//GEN-LAST:event_btnTimKiemTheoMaThuocActionPerformed

