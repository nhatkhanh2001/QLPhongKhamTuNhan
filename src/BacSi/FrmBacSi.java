    //Hàm lấy dữ liệu từ SQL đưa vào bảng bác sĩ 
    public void fillDataTable(){ 
        DefaultTableModel tbModel = (DefaultTableModel) TbDSBacSi.getModel();
        tbModel.setRowCount(0); // Xoá data trong table
        for (ClassBacSi bs : daoBS.getAllBacSi()){
            Object dataRow[] = new Object[6];
            dataRow[0] = bs.getMaBS();
            dataRow[1] = bs.getHoTenBS();
            dataRow[2] = date_format.format(bs.getNgaySinh()); // Sửa lại thứ tự dd/mm/yyyy 
            dataRow[3] = bs.getGioiTinh();
            dataRow[4] = bs.getDiaChi();
            dataRow[5] = bs.getSoDienThoai();
           
            tbModel.addRow(dataRow);
        }                       
    }
   
    // Hàm bool kiểm tra đúng, sai. Nhập đầy đủ thông tin mới cho thêm bác sĩ mới
    public boolean checkValidateForm(){
        if (txtMaBS.getText().isEmpty()||
            txtHoTenBS.getText().isEmpty()||
            txtNgaySinh.getText().isEmpty()||
            cbbGioiTinh.getSelectedItem().toString().isEmpty()||
            txtDiaChi.getText().isEmpty()||
            txtSDT.getText().length()<10)      
            
        {
           // JOptionPane.showMessageDialog(this, "Bạn chưa nhập đủ 10 sdt");
            return false;
        }
        else  
        {
            if (txtSDT.getText().length()>10)
            {
            // JOptionPane.showMessageDialog(this, "Bạn chưa nhập đủ 10 sdt");
             return false;
            }
            else {
                return true; 
            }                    
        }     
    }
    
    // Lấy dữ liệu đã nhập get vào TbBacSi
    public ClassBacSi getModel() throws ParseException{
        ClassBacSi bs = new ClassBacSi();
        bs.setMaBS(Integer.parseInt(txtMaBS.getText().trim()));
        bs.setHoTenBS(txtHoTenBS.getText().trim());
        bs.setNgaySinh(date_format.parse(txtNgaySinh.getText()));
        bs.setGioiTinh(cbbGioiTinh.getSelectedItem().toString());
        bs.setDiaChi(txtDiaChi.getText().trim());
        bs.setSoDienThoai(txtSDT.getText().trim());
            
        return bs;
    }
    
    // Đổ dữ liệu lên form, dùng cho envent Mouse Click và tìm kiếm 
    public void setModel (ClassBacSi bs){
        txtMaBS.setText(String.valueOf(bs.getMaBS()));
        txtHoTenBS.setText(bs.getHoTenBS());
        txtNgaySinh.setText(date_format.format(bs.getNgaySinh()));
        cbbGioiTinh.setSelectedItem(bs.getGioiTinh());
        txtDiaChi.setText(bs.getDiaChi());
        txtSDT.setText(bs.getSoDienThoai());
              
    }
    
    // Hàm thêm 1 bác sĩ mới
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
       if (!checkValidateForm())
       {
           JOptionPane.showMessageDialog(this, "Bạn chưa nhập đầy đủ thông tin hoặc sđt chưa đủ 10 số, không khoảng cách sđt!"); //Thanh thông báo hiện lên
           lbStatus.setText("Bạn chưa nhập đầy đủ thông tin hoặc sđt chưa đủ 10 số, không khoảng cách sđt!"); //Thông báo qua dòng labelStatus
           lbStatus.setForeground(Color.red);
       }
       else{
           try {
               ClassBacSi bs = getModel();
             //  System.out.println("=>"+bs.toString());
               if (daoBS.add(bs)>0) {
                   JOptionPane.showMessageDialog(this, "Thêm bác sĩ mới thành công!"); //Thanh thông báo hiện lên
                   lbStatus.setText("Thêm bác sĩ mới thành công!"); //Thông báo qua dòng labelStatus
                   lbStatus.setForeground(Color.blue);
                   fillDataTable();
                   LoadDataToArrayList(); 
               }
               else{
                   JOptionPane.showMessageDialog(this, "Thêm bác sĩ mới thất bại!"); 
                   lbStatus.setText("Thêm bác sĩ mới thất bại!");
                   lbStatus.setForeground(Color.red);
               }
           } catch (ParseException ex) {
               System.out.println("Error:"+ex.toString());
           }      
       }               
    }//GEN-LAST:event_btnThemActionPerformed

