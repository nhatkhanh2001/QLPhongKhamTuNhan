    // Hàm LoadDataToArrayList
    ArrayList<ClassBacSi> arr = new ArrayList<ClassBacSi>();
    public void LoadDataToArrayList()
    {
        arr.clear();
        try{
            String user="sa";
            String pass="123456";
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=DoAnJavaQLPhongKhamTuNhan";
            Connection con = DriverManager.getConnection(url, user, pass);
            String sql = "select * from Table_BacSi";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){ 
                int maBS = rs.getInt("MaBS");
                String hotenBS = rs.getString("HoTenBS");
                Date ngaysinh = rs.getDate("NgaySinh"); 
                String gioitinh = rs.getString("GioiTinh");
                String diachi = rs.getString("DiaChi");
                String sdt = rs.getString("SoDienThoai");
               
                ClassBacSi bacsi = new ClassBacSi(maBS, hotenBS, ngaysinh, gioitinh, diachi, sdt);
                arr.add(bacsi);
            }
            con.close();
        } 
        catch(Exception ex){
            System.out.println (ex.toString());
        }
    }
    
