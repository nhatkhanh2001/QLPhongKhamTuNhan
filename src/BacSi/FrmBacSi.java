    // Hàm tìm kiếm theo mã bác sĩ
    private void btnTimKiemTheoMaBSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemTheoMaBSActionPerformed
        if (txtMaBS.getText().isEmpty())
       {
           JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã bác sĩ!");
       }
    else {
        try{
            ClassBacSi findbs = daoBS.findBacSiByID(Integer.parseInt(txtMaBS.getText().trim()));
            
            if (findbs != null){
                JOptionPane.showMessageDialog(this, "Đã tìm thấy mã BS: "+txtMaBS.getText());
                lbStatus.setText("Đã tìm thấy mã bác sĩ: "+txtMaBS.getText());
                lbStatus.setForeground(Color.blue);
                setModel(findbs); // Tìm thấy mã bác sĩ sẽ đổ dữ liệu lên form
            }
            else {
                 JOptionPane.showMessageDialog(this, "Không tìm thấy mã BS: "+txtMaBS.getText());
                 lbStatus.setText("Không tìm thấy mã bác sĩ: "+txtMaBS.getText());
                 lbStatus.setForeground(Color.red);
            }
        } catch (Exception ex){
             System.out.println (ex.toString());
        }     
    }                 
    }//GEN-LAST:event_btnTimKiemTheoMaBSActionPerformed

